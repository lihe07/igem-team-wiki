<h1>Project Description</h1>

<p>
  Our project aims to resolve the problem of human ingestion of microplastics
  through drinking bottled water.
</p>
<p>
  According to Statista (2022), bottled water consumption in the U.S. has grown
  by almost 40% since 2010, reaching 44 gallons per person in 2019. This is not
  to mention those who buy bottled sodas and other beverages. Although 41% of
  people regard bottled water as the most trustworthy source of clean drinking
  water, the truth is actually quite the opposite. (Aqusana, 2021). A global
  survey based on drinking water quality has already demonstrated the fact that
  approximately 325 microplastics with the size of 6.5 um to 100 um are found in
  per liter of bottled water. The microplastics mainly diffused from bottle
  water’s PET bottle and HDPE cap during the storing and transport process.
  (Mason,.et al, 2018). In the long term, microplastics ingested through
  different routes can accumulate in the body and cause varying degrees of
  health damage, such as oxidative stress or hormonal disruption.
</p>
<p>
  Aiming to design a novel biofilm capable of addressing microplastics’ harm to
  humans, our team reviewed several existing materials with high performances
  that could be possibly applied to our innovation.
</p>
<p>
  One promising material we examined is Bacterial Cellulose Membrane (BCM), a
  commonly used bio-manufacturing material, which offers exceptional properties
  such as high strength, water retention, and shape adaptability.
</p>
<p>
  Currently, biomass charcoal, cellulose, chitin, and proteins like nano
  cellulose fibers and chitosan were identified as effective adsorbents for
  microplastics due to their high porosity and adsorption capacity (Zhu et al,
  2023.). Based on our application scenario, we chose to process and edit chitin
  or chitosan, which are eco- friendly and bio-friendly materials. We added
  these materials to our filter to increase its filtration ability.
</p>
<p>
  Furthermore, oat protein sponges (OPS), developed through crosslinking oat
  protein isolate, demonstrated excellent water adsorption capacity and
  efficient removal of polystyrene (PS) microparticles, a common type of
  microplastic (Zhenggang Wang et al.). We realized that oat protein also has
  the ability to attract microplastics due to hydrophobic interactions and
  electrostatic interaction from its side chains, thus decided to add oat
  protein as an additional component to our membrane. This connection could be
  achieved using cellulose binding module (CBM), which are contiguous amino acid
  sequences with a discreet fold having carbohydrate-binding activity that can
  combine cellulose and functional protein together.
</p>
<p>
  Drawing from these findings and the contributions of Links China, our team
  aims to develop innovative biofilms that efficiently absorb microplastics and
  ensure the provision of clean and safe drinking water. By leveraging the
  properties of BC membranes, chitin, oat protein, and CBM, we aim to design
  biofilms that effectively filter out microplastics from various water sources.
  This research collaboration between our team and Links China significantly
  contributes to addressing the growing concerns surrounding microplastic
  pollution and its impact on human health and the environment.
</p>

