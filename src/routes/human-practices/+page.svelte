<p>The Origin of Our Program:</p>

<p>
  We first learned about the microplastic issue from an article that briefly:
</p>

<p>
  These reports all briefly highlighted the widespread nature of microplastics,
  their source to reach human exposure, and their potential effects on human
  health. As a result, our team did extensive research on macro-level water
  microplastics and identified three potential avenues of action, briefly
  summarized as follows:
</p>
<ol>
  <li>
    Fundamentally reducing plastic production, similar to the efforts of
    previous teams like Team Exeter and Team Kyoto from
    <a href="https://2019.igem.org" target="_blank">https://2019.igem.org</a>,
    by developing alternative bio-environmental materials.
  </li>
  <li>
    Genetically engineering existing plastic recycling processes, akin to the
    approaches taken by previous teams such as Team TU_Kaiserslautern and Team
    Virginia from
    <a href="https://2019.igem.org" target="_blank">https://2019.igem.org</a>
    , by degrading microplastics.
  </li>
  <li>Directly reducing exposure through improved water filtration systems.</li>
</ol>
<p>
  Due to practical limitations (as a high school student team, we carefully
  considered our time and technology/equipment constraints) and the well-rounded
  precedents set by other teams, we decided to focus on the third option,
  briefly summarized as follows: improving existing water filtration systems.
</p>
<p>
  Water filtration is employed in various operational scenarios in our daily
  lives. Through research, we insititued the roadmap for water filtration (as
  shown in the diagram below), and discovered that water treatment plants
  actually play a pivotal role in the operation of cities and companies.
</p>
<p>
  Consequently, our initial goal was set as follows: starting from large-scale
  water filtration plants, we aimed to learn their filtration mechanisms and
  apply this technology to various operations, and therefore effectively filter
  out microplastics from water sources.
</p>
<p>
  After setting our goal, we visited the Jingkai Water Plant in Beijing&#39;s
  Yizhuang District, China. Through explanations provided by Deputy Plant
  Manager Zhang, we gained insights into the filtration mechanisms of Secondary
  Treatment and Tertiary Treatment: MBR (Membrane Bio-Reactor)
</p>
