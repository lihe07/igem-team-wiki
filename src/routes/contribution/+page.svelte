<p>
  1. We have developed a novel method for quantifying and qualitatively
  assessing the content of microplastics in samples, which is accessible on the
  measurement page.
</p>

<p>
  2. We created a part named "part X" by modifying the monomers proposed by
  Links China 2021, offering future teams valuable enhancements. Additionally,
  the creation of "part Y" involves the reapplication of Links China 2021's
  triblock structure, serving as a reference for teams working on
  materials-related projects.
</p>

<p>
  3. We conducted the first modeling of the adsorption of microplastics by
  bio-based materials, providing a foundational framework for future teams.
</p>

<p>
  4. We introduced a calculator program that enables consumers to estimate their
  daily intake of microplastics, aiming to raise awareness about microplastic
  ingestion and encourage individuals to reduce their reliance on plastic
  products in their daily lives.
</p>
